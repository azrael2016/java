package test_7_1;

public class Point {
	protected double x;
	protected double y;
	public Point(){
		x=0;
		y=0;
	}
	public double getX(){
		return x;
	}
	public double getY(){
		return y;
	}
	public void setX(double x){
		this.x=x;
	}
	public void setY(double y){
		this.y=y;
	}
}